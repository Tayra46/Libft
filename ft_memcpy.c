#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	int				i;
	unsigned char	*pdst;
	unsigned char	*psrc;

	pdst = (unsigned char *)dst;
	psrc = (unsigned char *)src;
	i = 0;
	while (pdst[i] && psrc[i] && i < n)
	{
		ft_memset(pdst, psrc[i], 1);
		i++;
	}
	return (dst);
}
