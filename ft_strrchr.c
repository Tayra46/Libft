#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int				i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			break ;
		i++;
	}
	return ((char *)&s[i]);
}
